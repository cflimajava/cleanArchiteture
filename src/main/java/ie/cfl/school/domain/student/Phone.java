package ie.cfl.school.domain.student;

public class Phone {
	
	private String ddd;
	private String number;
	
	public Phone(String ddd, String number) {
		
		if(ddd == null || !ddd.matches("\\d{2}")) {
			throw new IllegalArgumentException("Phone's DDD wrong format");
		}
		this.ddd = ddd;

		
		if(ddd == null || !number.matches("\\d{8}|\\d{9}")) {
			throw new IllegalArgumentException("Phone's number wrong format");
		}
		this.number = number;
	}

	public String getDdd() {
		return ddd;
	}

	public void setDdd(String ddd) {
		this.ddd = ddd;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((ddd == null) ? 0 : ddd.hashCode());
		result = prime * result + ((number == null) ? 0 : number.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Phone other = (Phone) obj;
		if (ddd == null) {
			if (other.ddd != null)
				return false;
		} else if (!ddd.equals(other.ddd))
			return false;
		if (number == null) {
			if (other.number != null)
				return false;
		} else if (!number.equals(other.number))
			return false;
		return true;
	}

}
